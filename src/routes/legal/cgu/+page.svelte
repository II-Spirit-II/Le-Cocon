    </p>
  </section>

  <section class="mb-8">
    <h2 class="text-lg font-semibold text-warm-800 mb-3">Article 10 — Protection des données personnelles</h2>
    <p class="text-warm-600 leading-relaxed">
      Le traitement des données personnelles est régi par la
      <a href="/legal/confidentialite" class="text-miel-600 hover:text-miel-700 underline">politique de confidentialité</a>
