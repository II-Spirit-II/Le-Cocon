<h1 class="text-2xl font-display font-bold text-warm-900 mb-6">Conditions Générales d'Utilisation</h1>

<p class="text-sm text-warm-400 mb-8">Dernière mise à jour : 22 mars 2026 — Version 1.0</p>

<div class="prose prose-warm max-w-none">
  <section class="mb-8">
    <h2 class="text-lg font-semibold text-warm-800 mb-3">Article 1 — Objet</h2>
    <p class="text-warm-600 leading-relaxed">
      Les présentes Conditions Générales d'Utilisation (ci-après "CGU") définissent les modalités
      d'accès et d'utilisation du service Le Cocon, application de cahier de liaison numérique
      destinée aux professionnels de la petite enfance et aux parents d'enfants accueillis.
    </p>
    <p class="text-warm-600 leading-relaxed mt-2">
      Le Cocon permet aux assistantes maternelles et aux structures d'accueil de tenir un suivi
      quotidien des enfants (repas, siestes, humeur, santé, activités) et de le partager de manière
      sécurisée avec les parents concernés.
    </p>
    <p class="text-warm-600 leading-relaxed mt-2">
      L'utilisation du service implique l'acceptation pleine et entière des présentes CGU.
    </p>
  </section>

  <section class="mb-8">
    <h2 class="text-lg font-semibold text-warm-800 mb-3">Article 2 — Définitions</h2>
    <ul class="list-disc list-inside text-warm-600 mt-2 space-y-1">
      <li><strong>"Service"</strong> : l'application web Le Cocon et l'ensemble de ses fonctionnalités</li>
      <li><strong>"Opérateur"</strong> : l'entité responsable de l'exploitation du Service, identifiée dans les mentions légales</li>
      <li><strong>"Utilisateur"</strong> : toute personne disposant d'un compte sur le Service, qu'elle soit professionnel de la petite enfance ("assistante") ou parent</li>
      <li><strong>"Enfant"</strong> : mineur dont les données sont saisies dans le Service dans le cadre du suivi quotidien</li>
      <li><strong>"Carnets quotidiens"</strong> : fiches de suivi journalier comprenant les informations relatives aux repas, siestes, humeur, santé et observations</li>
    </ul>
  </section>

  <section class="mb-8">
    <h2 class="text-lg font-semibold text-warm-800 mb-3">Article 3 — Inscription et accès au service</h2>
    <p class="text-warm-600 leading-relaxed">
      L'accès au Service nécessite la création d'un compte utilisateur. Lors de l'inscription,
      l'utilisateur fournit son nom, son adresse email et un mot de passe. Il s'engage à :
    </p>
    <ul class="list-disc list-inside text-warm-600 mt-2 space-y-1">
      <li>Fournir des informations exactes et à jour</li>
      <li>Maintenir la confidentialité de ses identifiants de connexion</li>
      <li>Informer immédiatement l'Opérateur de toute utilisation non autorisée de son compte</li>
    </ul>
    <p class="text-warm-600 leading-relaxed mt-2">
      Les parents accèdent au suivi de leur enfant via un code d'invitation généré par l'assistante
      maternelle référente. Ce code est à usage unique et à durée limitée.
    </p>
  </section>

  <section class="mb-8">
    <h2 class="text-lg font-semibold text-warm-800 mb-3">Article 4 — Données relatives aux enfants</h2>
    <p class="text-warm-600 leading-relaxed">
      Le Service traite des données à caractère personnel relatives à des mineurs. À ce titre :
    </p>
    <ul class="list-disc list-inside text-warm-600 mt-2 space-y-1">
      <li>La saisie de données concernant un enfant est effectuée sous la responsabilité de
        l'assistante maternelle ou du parent titulaire de l'autorité parentale</li>
      <li>L'inscription d'un enfant dans le Service vaut consentement au traitement de ses données
        dans le cadre strict du suivi quotidien</li>
      <li>L'accès aux données d'un enfant est strictement limité aux professionnels référents et
        aux parents autorisés par le système de codes d'invitation</li>
      <li>Aucune donnée relative aux enfants n'est accessible publiquement, partagée avec des tiers
        ou utilisée à des fins autres que le suivi quotidien</li>
      <li>Les parents peuvent à tout moment exercer leurs droits d'accès, de rectification et de
        suppression des données de leur enfant conformément à la
        <a href="/legal/confidentialite" class="text-miel-600 hover:text-miel-700 underline">politique de confidentialité</a></li>
    </ul>
  </section>

  <section class="mb-8">
    <h2 class="text-lg font-semibold text-warm-800 mb-3">Article 5 — Obligations des utilisateurs</h2>
    <p class="text-warm-600 leading-relaxed">
      L'utilisateur s'engage à utiliser le Service de manière responsable et conformément à sa
      destination. Sont notamment interdits :
    </p>
    <ul class="list-disc list-inside text-warm-600 mt-2 space-y-1">
      <li>La diffusion de contenus illicites, injurieux, diffamatoires ou portant atteinte à la vie privée d'autrui</li>
      <li>La collecte ou l'extraction de données d'autres utilisateurs</li>
      <li>Toute tentative de contournement des mesures de sécurité du Service</li>
      <li>L'utilisation du Service à des fins sans rapport avec le suivi des enfants accueillis</li>
      <li>Le partage de ses identifiants de connexion avec un tiers</li>
    </ul>
    <p class="text-warm-600 leading-relaxed mt-2">
      L'Opérateur se réserve le droit de suspendre ou de supprimer tout compte ne respectant pas
      les présentes CGU, après mise en demeure restée sans effet.
    </p>
  </section>

  <section class="mb-8">
    <h2 class="text-lg font-semibold text-warm-800 mb-3">Article 6 — Assistant IA</h2>
    <p class="text-warm-600 leading-relaxed">
      Le Service intègre un assistant fondé sur l'intelligence artificielle, destiné à aider les
      utilisateurs dans le suivi quotidien des enfants (synthèses, réponses aux questions, aide à
      la saisie).
    </p>
    <ul class="list-disc list-inside text-warm-600 mt-2 space-y-1">
      <li>L'assistant IA est alimenté par l'API Scaleway Generative (modèle Mistral), hébergée en
        France. Aucune donnée n'est transmise en dehors de l'Union européenne.</li>
      <li>Seules les données de l'utilisateur connecté et des enfants qui lui sont associés sont
        utilisées pour générer les réponses.</li>
      <li>Les réponses de l'assistant sont fournies à titre informatif et ne sauraient se substituer
        à un avis médical, paramédical ou juridique.</li>
      <li>L'Opérateur ne garantit ni l'exactitude ni l'exhaustivité des réponses générées par
        l'assistant IA. L'utilisateur demeure seul responsable de l'usage qu'il en fait.</li>
      <li>L'assistant IA peut être désactivé à tout moment par l'Opérateur sans préavis.</li>
    </ul>
  </section>

  <section class="mb-8">
    <h2 class="text-lg font-semibold text-warm-800 mb-3">Article 7 — Propriété intellectuelle</h2>
    <p class="text-warm-600 leading-relaxed">
